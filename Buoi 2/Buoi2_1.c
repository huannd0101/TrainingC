#include<stdio.h>
#include<math.h>

int main() {
	/*
		sqrt(a): can bac 2 cua a
		exp(x): e^x
		abs(x): tri tuyet doi cua x: |x| (so nguyen)
		fabs(x): tri tuyet doi cua so thuc x
		pow(a, b): a^b
	*/
	printf("%f\n", sqrt(5));
	printf("%f\n", exp(2));
	printf("%d\n", abs(-5));
	printf("%f\n", fabs(-5.5));
	printf("%f\n", pow(2, 2));
	
	return 0;
}
